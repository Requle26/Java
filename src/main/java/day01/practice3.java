//더하기 : 15
//빼기 : 5
//곱하기 : 50
//나누기 : 2

public class practice3 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 5;

        System.out.println("더하기 : " + (num1 + num2));
        System.out.println("빼기 : " + (num1 - num2));
        System.out.println("곱하기 : " + (num1 * num2));
        System.out.println("나누기 : " + (num1 / num2));
    }
}
